
// Numeric string used with operators like +, - , / , * returns number type.

let s
s = '4' - '4'; 
console.log(s); // 0
s = '4' * 5;
console.log(s); // 20

// Boolean Conversion to Number:- 
// 	If we use Boolean, true is considered as 1 and false is considered as 0.

let r;
r = '5' - true;
console.log(r); // 4
r = 10 + false;
console.log(r); // 10